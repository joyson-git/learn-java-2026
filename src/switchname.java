public class switchname {
    public static void  main(String[] args){
        int a = 2;
        switch (a) {
            case 1:
                System.out.print(" the number 1 ");
                break;
            case 2:
                System.out.print(" the number 2");
                break;
            case 3:
                System.out.print(" the number 3 ");
                break;
            default:
                System.out.print(" no number");
        }

    }
}
